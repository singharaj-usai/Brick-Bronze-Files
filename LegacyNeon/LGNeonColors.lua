function getnewpart(part)
	if part:IsA("Part") or part:IsA("WedgePart") or part:IsA("CornerWedgePart") or part:IsA("TrussPart") or part:IsA("BasePart") or part:IsA("MeshPart") then
		changecolors(part)
		part:GetPropertyChangedSignal("Color"):Connect(function()
			changecolors(part)
		end)
		part:GetPropertyChangedSignal("Material"):Connect(function()
			changecolors(part)
		end)
	end
end

game.workspace.DescendantAdded:Connect(function(part)
	for i,v in pairs(game.workspace:GetDescendants()) do
		getnewpart(v)
	end
end)